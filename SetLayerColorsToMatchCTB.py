import rhinoscriptsyntax as rs
import re
import System.Drawing.Color

def SetAllLayerColors():
    for layer in rs.LayerNames():
        layer_name_re = re.search("AR[0-9]+", layer)
        if layer_name_re:
            ctb_layer_code = layer_name_re.group()
            
            layer_color_str = ctb_colors[ctb_layer_code]
            layer_color = System.Drawing.Color.FromArgb(int(layer_color_str.split(",")[0]),
                                            int(layer_color_str.split(",")[1]),
                                            int(layer_color_str.split(",")[2]))
            rs.LayerColor(layer, color=layer_color)

